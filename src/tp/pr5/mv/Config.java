package tp.pr5.mv;

public class Config {

	private String asmRoute;
	private String inRoute;
	private int mode = 0;
	private String outRoute;
	
	public String getAsmRoute() {
		return asmRoute;
	}
	public void setAsmRoute(String a) {
		asmRoute = a;
	}
	
	
	public String getInRoute() {
		return inRoute;
	}
	public void setInRoute(String i) {
		inRoute = i;
	}
	
	
	public int getMode() {
		return mode;
	}
	public void setMode(int m) {
		mode = m;
	}
	
	
	public String getOutRoute() {
		return outRoute;
	}
	public void setOutRoute(String o) {
		outRoute = o;
	}
	
	
	
	
	
	
	
	
}
